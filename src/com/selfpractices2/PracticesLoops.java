package com.selfpractices2;

public class PracticesLoops {
	public static void main(String[] args) {

		for(int i=1;i<=5;i++) {
			System.out.println("*******************");	
		}
		System.out.println("//////////////////");	
		int i=1;
		while(i<=5) {
			System.out.println("*******************");	
			i++;
		}
		System.out.println("/////////////////////");
		
		int y =1;
		do {
			
			System.out.println("*******************");
			
			y++;
			
		}while(y<=5);

		int num1=4;
		do {
			System.out.println("Hello");
			num1++;
		}while(num1<=5);
	}
}
